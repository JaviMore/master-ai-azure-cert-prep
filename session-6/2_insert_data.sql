-- EXTRA HISTORICAL ORDERS (2022–2024)
-- Purpose: add more data to visualize Total Revenue per Year
-- =====================================================================

-- 80 new orders distributed across 2022–2024
DECLARE @i INT = 1;
WHILE @i <= 80
BEGIN
    DECLARE @cust INT = (SELECT TOP 1 CustomerID FROM dbo.Customers ORDER BY NEWID());
    DECLARE @status NVARCHAR(30) = (SELECT TOP 1 v FROM (VALUES ('Paid'), ('Shipped'), ('Cancelled')) AS t(v) ORDER BY NEWID());
    DECLARE @orderDate DATETIME2 = DATEADD(DAY, -ABS(CHECKSUM(NEWID())) % 365, DATEFROMPARTS(2022 + (ABS(CHECKSUM(NEWID())) % 3), 1 + (ABS(CHECKSUM(NEWID())) % 12), 1 + (ABS(CHECKSUM(NEWID())) % 28)));

    INSERT INTO dbo.Orders (CustomerID, OrderDate, Status, TotalAmount)
    VALUES (@cust, @orderDate, @status, 0);

    SET @i += 1;
END;
GO

-- Add random Order Details for these new orders
DECLARE @orderId INT, @productId INT, @q INT, @p DECIMAL(10,2);
DECLARE order_cursor CURSOR FOR SELECT OrderID FROM dbo.Orders WHERE YEAR(OrderDate) < 2025;
OPEN order_cursor;
FETCH NEXT FROM order_cursor INTO @orderId;
WHILE @@FETCH_STATUS = 0
BEGIN
    DECLARE @lines INT = 2 + ABS(CHECKSUM(NEWID())) % 4;
    DECLARE @l INT = 1;
    WHILE @l <= @lines
    BEGIN
        SET @productId = (SELECT TOP 1 ProductID FROM dbo.Products ORDER BY NEWID());
        SET @q = 1 + ABS(CHECKSUM(NEWID())) % 4;
        SET @p = (SELECT UnitPrice FROM dbo.Products WHERE ProductID = @productId);

        INSERT INTO dbo.OrderDetails (OrderID, ProductID, Quantity, UnitPrice)
        VALUES (@orderId, @productId, @q, @p);

        SET @l += 1;
    END;
    FETCH NEXT FROM order_cursor INTO @orderId;
END;
CLOSE order_cursor;
DEALLOCATE order_cursor;
GO


-- --------------------------------------------------------------
-- UPDATE TOTAL AMOUNTS FOR EACH ORDER
-- --------------------------------------------------------------
UPDATE o
SET TotalAmount = totals.SumAmount
FROM dbo.Orders o
CROSS APPLY (
    SELECT SUM(od.Quantity * od.UnitPrice) AS SumAmount
    FROM dbo.OrderDetails od
    WHERE od.OrderID = o.OrderID
) AS totals;
GO

